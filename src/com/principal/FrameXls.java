     public void relatorio(){
        boolean geraArq = false;
        
        File arquivo;
        String patch = "";
        JFileChooser file = new JFileChooser();
        file.setFileSelectionMode(JFileChooser.FILES_ONLY);
        
        int x = file.showSaveDialog(null);
        if(x == 1){
            JOptionPane.showMessageDialog(null, "Nenhum arquivo selecionado!");
        }else{
            arquivo = file.getSelectedFile();
            patch = arquivo.getPath();
            patch +=".txt";
            geraArq = true;
        }
        if(geraArq){
            try{
                 FileWriter writer = new FileWriter(patch);
                 writer.append("Data");
                 writer.append(",");
                 writer.append("Rota");
                 writer.append(",");
                 writer.append("Horario Lib CPD");
                 writer.append(",");
                 writer.append("Impressora");
                 writer.append(",");
                 writer.append("Danfe Normal");
                 writer.append(",");
                 writer.append("Danfe Epec");
                 writer.append(",");
                 writer.append("Operador");
                 writer.append("\n");
                 
                 List<Demonstrativo> pick = DemonstrativoJpaDAO.getInstance().findByData(DataTxt.getText());
                 
                  for(int b = 0; b < pick.size(); b++) {
                     writer.append(pick.get(b).getData());
                     writer.append(",");
                     writer.append(pick.get(b).getRota());
                     writer.append(",");
                     writer.append(pick.get(b).getLiberacaocpd());
                     writer.append(",");
                     writer.append(pick.get(b).getImpressora());
                     writer.append(",");
                     writer.append(pick.get(b).getDanfenormal());
                     writer.append(",");
                     writer.append(pick.get(b).getDanfeepec());
                     writer.append(",");
                     writer.append(pick.get(b).getOperador());
                     writer.append("\n");
                    
                }
                 writer.flush();
                 writer.close();
                 JOptionPane.showMessageDialog(null, "Arquivo Gerado com sucesso!");
                 FrameXls.super.dispose();
            }catch(Exception e){
                e.printStackTrace();
            }       
        }           
    }
    

